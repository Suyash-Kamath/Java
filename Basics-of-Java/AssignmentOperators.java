public class AssignmentOperators {
    public static void main(String[] args) {
        int a = 10;    // Simple assignment
        a += 5;         // a = a + 5
        a *= 2;         // a = a * 2
        a /= 4;         // a = a / 4
        a %= 3;         // a = a % 3
        
        System.out.println("Value of a: " + a);
    }
}
