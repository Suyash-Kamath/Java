public class LogicalOperators {
    public static void main(String[] args) {
        boolean a = true, b = false;
        
        System.out.println(a && b);  // AND (both must be true)
        System.out.println(a || b);  // OR (one must be true)
        System.out.println(!a);      // NOT (reverses the boolean value)
    }
}
